def countdown(secs)
  counter = secs
  while counter != 0 
    puts "#{counter} SECOND(S)!"
      counter -= 1
  end
  "HAPPY NEW YEAR!"
end

def countdown_with_sleep(delay)
  sleep delay
    counter = secs
  while counter != 0 
    puts "#{counter} SECOND(S)!"
      counter -= 1
  end
  "HAPPY NEW YEAR!"
end